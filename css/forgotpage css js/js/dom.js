function makeLine(length) {
  let line = '';
  for (let x = 1; x <= length; x++) {
    line += '* ';
  }
  return `${line}\n`;
}

function makeTriangle(num) {
  let str = '';
  for (let y = 1; y <= num; y++) {
    str += makeLine(y);
  }
  return str;
}

